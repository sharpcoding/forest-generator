export interface ITree {
  /**
   * Column in sprite where the tree comes from
   */
  spriteColumn: number;
  /**
   * Row in sprite where the tree comes from
   */
  spriteRow:  number;
  /**
   * Tree position (x) on canvas
   */
  x: number;
  /**
   * Tree position (y) on canvas
   */
  y: number;
}